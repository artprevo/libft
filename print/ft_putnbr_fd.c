#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putchar_fd('-');
		ft_putchar_fd('2');
		ft_putnbr_fd(14748364);
		n = 8;
	}
	if (n < 0)
	{
		ft_putchar_fd('-');
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n/10);
	ft_putchar_fd(n % 10 + 48);
}
